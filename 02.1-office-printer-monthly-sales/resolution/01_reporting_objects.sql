    if l_count = 0 then
        execute immediate q'[
            create index idx_poc_mth_rep_log_main
                on poc_monthly_report_run_log (
                    report_code,
                    month_start,
                    requested_by,
                    generated_at
                )
        ]';
    end if;
end;
